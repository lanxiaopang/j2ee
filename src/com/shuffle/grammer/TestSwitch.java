package com.shuffle.grammer;

public class TestSwitch {

	/**
	 * @Title: main
	 * @Description: switch
	 * @param args
	 * @return void
	 * @author sunzhongyi
	 * @throws
	 */
	public static void main(String[] args) {
		int i = 18;
		switch(i){
			case 2:
				System.out.println("2");
				break;
			case 18:
				System.out.println("18");
				break;
			default:
				System.out.println("default");
		}
	}

}
